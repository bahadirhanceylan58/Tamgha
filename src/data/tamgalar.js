export function findKartById(id) {
  if (id === 'yada_tasi') return YADA_TASI;
  const hayvan = HAYVANLAR.find((h) => h.id === id);
  if (hayvan) return hayvan;
  const mit = MITOLOJI.find((m) => m.id === id);
  if (mit) return mit;
  return TAMGALAR.find((t) => t.id === id) || null;
}

export function getBolgeTamgalari(bolgeId) {
  const tumKartlar = [...TAMGALAR, ...HAYVANLAR, ...MITOLOJI, YADA_TASI];
  return tumKartlar.filter((t) => t.bolge === bolgeId);
}

export function generateQuestions(bolgeId, count = 5) {
  const bolgeTamgalari = getBolgeTamgalari(bolgeId);
  const questions = [];

  const shuffled = [...bolgeTamgalari].sort(() => Math.random() - 0.5);

  for (let i = 0; i < count; i++) {
    const correct = shuffled[i % shuffled.length];
    const tip = i % 2 === 0 ? 'tamga_to_ses' : 'ses_to_tamga';

    // Yanlış seçenekler her zaman sadece standart TAMGALAR'dan gelir
    const yanlisPotansiyel = TAMGALAR.filter((t) => t.id !== correct.id);
    const wrong = yanlisPotansiyel.sort(() => Math.random() - 0.5).slice(0, 3);

    const options = [correct, ...wrong].sort(() => Math.random() - 0.5);

    questions.push({ tip, correct, options, id: i });
  }

  return questions;
}
