// Cinsiyete göre filtrele
export function filtreIsimler(cinsiyet) {
  if (cinsiyet === 'hepsi') return ISIMLER;
  if (cinsiyet === 'i') return ISIMLER.filter((i) => i.cinsiyet === 'i' || i.cinsiyet === cinsiyet);
  return ISIMLER.filter((i) => i.cinsiyet === cinsiyet || i.cinsiyet === 'i');
}

// Rastgele isim seç
export function rastgeleIsim(cinsiyet = 'hepsi') {
  const liste = filtreIsimler(cinsiyet);
  return liste[Math.floor(Math.random() * liste.length)];
}
